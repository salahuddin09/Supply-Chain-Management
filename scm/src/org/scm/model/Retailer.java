package org.scm.model;

/**
*
* @author Personal
*/
public class Retailer {
   private int retailer_ID;
    private String retailer_Name;
   private String retailer_Address;
   private String retailer_Contact_No;
   private String retailer_Email;

   /**
    * @return the retailer_Name
    */
   public String getRetailer_Name() {
       return retailer_Name;
   }

   /**
    * @param retailer_Name the retailer_Name to set
    */
   public void setRetailer_Name(String retailer_Name) {
       this.retailer_Name = retailer_Name;
   }

   /**
    * @return the retailer_Address
    */
   public String getRetailer_Address() {
       return retailer_Address;
   }

   /**
    * @param retailer_Address the retailer_Address to set
    */
   public void setRetailer_Address(String retailer_Address) {
       this.retailer_Address = retailer_Address;
   }

   /**
    * @return the retailer_Contact_No
    */
   public String getRetailer_Contact_No() {
       return retailer_Contact_No;
   }

   /**
    * @param retailer_Contact_No the retailer_Contact_No to set
    */
   public void setRetailer_Contact_No(String retailer_Contact_No) {
       this.retailer_Contact_No = retailer_Contact_No;
   }

   /**
    * @return the retailer_Email
    */
   public String getRetailer_Email() {
       return retailer_Email;
   }

   /**
    * @param retailer_Email the retailer_Email to set
    */
   public void setRetailer_Email(String retailer_Email) {
       this.retailer_Email = retailer_Email;
   }

   /**
    * @return the retailer_ID
    */
   public int getRetailer_ID() {
       return retailer_ID;
   }

   /**
    * @param retailer_ID the retailer_ID to set
    */
   public void setRetailer_ID(int retailer_ID) {
       this.retailer_ID = retailer_ID;
   }
}
