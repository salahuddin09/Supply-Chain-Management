package org.scm.model;

/**
*
* @author Personal
*/
public class Supplier {
   private int supplier_ID;
   private String supplier_Name;
   private String supplier_Address;
   private String supplier_Contact_No;
   private String supplier_Email;

   /**
    * @return the supplier_ID
    */
   public int getSupplier_ID() {
       return supplier_ID;
   }

   /**
    * @param supplier_ID the supplier_ID to set
    */
   public void setSupplier_ID(int supplier_ID) {
       this.supplier_ID = supplier_ID;
   }

   /**
    * @return the supplier_Name
    */
   public String getSupplier_Name() {
       return supplier_Name;
   }

   /**
    * @param supplier_Name the supplier_Name to set
    */
   public void setSupplier_Name(String supplier_Name) {
       this.supplier_Name = supplier_Name;
   }

   /**
    * @return the supplier_Address
    */
   public String getSupplier_Address() {
       return supplier_Address;
   }

   /**
    * @param supplier_Address the supplier_Address to set
    */
   public void setSupplier_Address(String supplier_Address) {
       this.supplier_Address = supplier_Address;
   }

   /**
    * @return the supplier_Contact_No
    */
   public String getSupplier_Contact_No() {
       return supplier_Contact_No;
   }

   /**
    * @param supplier_Contact_No the supplier_Contact_No to set
    */
   public void setSupplier_Contact_No(String supplier_Contact_No) {
       this.supplier_Contact_No = supplier_Contact_No;
   }

   /**
    * @return the supplier_Email
    */
   public String getSupplier_Email() {
       return supplier_Email;
   }

   /**
    * @param supplier_Email the supplier_Email to set
    */
   public void setSupplier_Email(String supplier_Email) {
       this.supplier_Email = supplier_Email;
   }
}
