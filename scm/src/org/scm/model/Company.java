package org.scm.model;

public class Company {
    private int company_ID;
    private String company_Name;
    private String company_Address;
    private String company_Contact_No;
    private String company_Email;

    /**
     * @return the company_ID
     */
    public int getCompany_ID() {
        return company_ID;
    }

    /**
     * @param company_ID the company_ID to set
     */
    public void setCompany_ID(int company_ID) {
        this.company_ID = company_ID;
    }

    /**
     * @return the company_Name
     */
    public String getCompany_Name() {
        return company_Name;
    }

    /**
     * @param company_Name the company_Name to set
     */
    public void setCompany_Name(String company_Name) {
        this.company_Name = company_Name;
    }

    /**
     * @return the company_Address
     */
    public String getCompany_Address() {
        return company_Address;
    }

    /**
     * @param company_Address the company_Address to set
     */
    public void setCompany_Address(String company_Address) {
        this.company_Address = company_Address;
    }

    /**
     * @return the company_Contact_No
     */
    public String getCompany_Contact_No() {
        return company_Contact_No;
    }

    /**
     * @param company_Contact_No the company_Contact_No to set
     */
    public void setCompany_Contact_No(String company_Contact_No) {
        this.company_Contact_No = company_Contact_No;
    }

    /**
     * @return the company_Email
     */
    public String getCompany_Email() {
        return company_Email;
    }

    /**
     * @param company_Email the company_Email to set
     */
    public void setCompany_Email(String company_Email) {
        this.company_Email = company_Email;
    }
}
