package org.scm.model;

public class Customer {
    private int customer_ID;
    private String customer_Name;
    private String customer_Address;
    private String customer_Contact_No;
    private String customer_Email;

    /**
     * @return the employee_ID
     */
    public int getCustomer_ID() {
        return customer_ID;
    }

    /**
     * @param customer_ID the customer_ID to set
     */
    public void setCustomer_ID(int customer_ID) {
        this.customer_ID = customer_ID;
    }

    /**
     * @return the customer_Name
     */
    public String getCustomer_Name() {
        return customer_Name;
    }

    /**
     * @param customer_Name the customer_Name to set
     */
    public void setCustomer_Name(String customer_Name) {
        this.customer_Name = customer_Name;
    }

    /**
     * @return the customer_Address
     */
    public String getCustomer_Address() {
        return customer_Address;
    }

    /**
     * @param customer_Address the customer_Address to set
     */
    public void setCustomer_Address(String customer_Address) {
        this.customer_Address = customer_Address;
    }

    /**
     * @return the customer_Contact_No
     */
    public String getCustomer_Contact_No() {
        return customer_Contact_No;
    }

    /**
     * @param customer_Contact_No the customer_Contact_No to set
     */
    public void setCustomer_Contact_No(String customer_Contact_No) {
        this.customer_Contact_No = customer_Contact_No;
    }

    /**
     * @return the customer_Email
     */
    public String getCustomer_Email() {
        return customer_Email;
    }

    /**
     * @param customer_Email the customer_Email to set
     */
    public void setCustomer_Email(String customer_Email) {
        this.customer_Email = customer_Email;
    }
}
